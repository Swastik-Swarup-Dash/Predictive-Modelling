def plot_predictions(
    actual: list, predictions: Dict[str, list], dates: list
) -> go.Figure:
    """Plot actual vs predicted prices"""
    fig = go.Figure()

    # Actual prices
    fig.add_trace(
        go.Scatter(
            x=dates[: len(actual)],
            y=actual,
            mode="lines",
            name="Actual",
            line=dict(color="#00FF00", width=2),
        )
    )

    colors = ["#FF6B6B", "#4ECDC4", "#45B7D1"]

    for i, (model_name, preds) in enumerate(predictions.items()):
        fig.add_trace(
            go.Scatter(
                x=dates[: len(preds)],
                y=preds,
                mode="lines",
                name=f"{model_name} Prediction",
                line=dict(color=colors[i % len(colors)], width=2, dash="dash"),
            )
        )

    fig.update_layout(
        title="Actual vs Predicted Prices",
        xaxis_title="Date",
        yaxis_title="Price (USD)",
        template=get_plotly_template(),
        height=400,
        hovermode="x unified",
    )

    return fig


def plot_metrics_comparison(metrics: List[Dict]) -> go.Figure:
    """Create metrics comparison chart"""
    normalized_metrics = normalize_metrics_payload(metrics)
    if not normalized_metrics:
        return go.Figure()

    df = pd.DataFrame(normalized_metrics)

    fig = make_subplots(
        rows=1,
        cols=3,
        subplot_titles=("MAE", "RMSE", "MAPE (%)"),
        horizontal_spacing=0.1,
    )

    models = df["model_type"].tolist()
    colors = ["#FF6B6B", "#4ECDC4", "#45B7D1"][: len(models)]

    # MAE
    fig.add_trace(
        go.Bar(x=models, y=df["mae"], name="MAE", marker_color=colors), row=1, col=1
    )

    # RMSE
    fig.add_trace(
        go.Bar(x=models, y=df["rmse"], name="RMSE", marker_color=colors), row=1, col=2
    )

    # MAPE
    fig.add_trace(
        go.Bar(x=models, y=df["mape"], name="MAPE", marker_color=colors), row=1, col=3
    )

    fig.update_layout(
        title="Model Performance Comparison",
        template=get_plotly_template(),
        showlegend=False,
        height=350,
    )

    return fig


def plot_forecast_with_confidence(
    dates: list, predictions: list, lower: list, upper: list
) -> go.Figure:
    """Plot forecast with confidence intervals"""
    fig = go.Figure()

    # Confidence interval
    fig.add_trace(
        go.Scatter(
            x=dates + dates[::-1],
            y=upper + lower[::-1],
            fill="toself",
            fillcolor="rgba(255, 215, 0, 0.2)",
            line=dict(color="rgba(255,255,255,0)"),
            name="Confidence Interval",
        )
    )

    # Predictions
    fig.add_trace(
        go.Scatter(
            x=dates,
            y=predictions,
            mode="lines",
            name="Prediction",
            line=dict(color="#FFD700", width=2),
        )
    )

    fig.update_layout(
        title="Price Forecast with Confidence Interval",
        xaxis_title="Date",
        yaxis_title="Price (USD)",
        template=get_plotly_template(),
        height=400,
    )

    return fig


