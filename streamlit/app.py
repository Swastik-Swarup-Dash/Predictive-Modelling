def normalize_metrics_payload(metrics: Any) -> List[Dict[str, Any]]:
    """Normalize API metrics payload into a list of records for plotting/table views."""
    if metrics is None:
        return []

    normalized: List[Dict[str, Any]] = []

    if isinstance(metrics, list):
        for item in metrics:
            if isinstance(item, dict):
                normalized.append(item)
    elif isinstance(metrics, dict):
        if {"model_type", "mae", "rmse", "mape"}.issubset(metrics.keys()):
            normalized.append(metrics)
        elif "metrics" in metrics and isinstance(metrics["metrics"], dict):
            for model_name, values in metrics["metrics"].items():
                if isinstance(values, dict):
                    normalized.append({"model_type": model_name, **values})
        else:
            for model_name, values in metrics.items():
                if isinstance(values, dict) and {"mae", "rmse", "mape"}.issubset(
                    values.keys()
                ):
                    normalized.append({"model_type": model_name, **values})

    sanitized: List[Dict[str, Any]] = []
    for row in normalized:
        model_name = row.get("model_type")
        mae = row.get("mae")
        rmse = row.get("rmse")
        mape = row.get("mape")
        if model_name is None:
            continue
        try:
            sanitized.append(
                {
                    "model_type": str(model_name),
                    "mae": float(mae),
                    "rmse": float(rmse),
                    "mape": float(mape),
                }
            )
        except (TypeError, ValueError):
            continue

    return sanitized


def normalize_trained_forecasts_payload(payload: Any) -> List[Dict[str, Any]]:
    """Normalize trained forecasts payload into a consistent list shape."""
    records: List[Dict[str, Any]] = []

    if isinstance(payload, list):
        for item in payload:
            if isinstance(item, dict):
                records.append(item)
    elif isinstance(payload, dict):
        for name, values in payload.items():
            if not isinstance(values, dict):
                continue
            records.append({"model_type": name, **values})

    normalized: List[Dict[str, Any]] = []
    for row in records:
        model_type = row.get("model_type")
        dates = row.get("dates") or []
        predictions = row.get("predictions") or []
        if not model_type or not isinstance(dates, list) or not isinstance(predictions, list):
            continue

        usable_horizon = min(len(dates), len(predictions))
        if usable_horizon == 0:
            continue

        try:
            normalized.append(
                {
                    "model_type": str(model_type),
                    "horizon": int(row.get("horizon", usable_horizon)),
                    "dates": [str(item) for item in dates[:usable_horizon]],
                    "predictions": [float(item) for item in predictions[:usable_horizon]],
                    "source": str(row.get("source", "trained_results")),
                }
            )
        except (TypeError, ValueError):
            continue

    return normalized


